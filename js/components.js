  icon(name, size = 20) {
    return `<span class="icon" style="width:${size}px;height:${size}px;display:inline-flex">${this.icons[name] || ''}</span>`;
  },

  /* ── Toast System ──────────────────────────────────── */
  showToast(message, type = 'info', duration = 4000) {
    const container = document.getElementById('toast-container');
    if (!container) return;
    const iconMap = { success: 'check', warning: 'alert', error: 'alert', info: 'info' };
    const toast = document.createElement('div');
    toast.className = `toast toast-${type}`;
    toast.innerHTML = `
      <span class="toast-icon">${this.icon(iconMap[type] || 'info', 18)}</span>
      <span class="toast-message">${message}</span>
      <button class="toast-close" onclick="this.parentElement.remove()">${this.icon('x', 14)}</button>
    `;
    container.appendChild(toast);
    setTimeout(() => {
      toast.style.opacity = '0';
      toast.style.transform = 'translateX(30px)';
      toast.style.transition = 'all 0.3s ease';
      setTimeout(() => toast.remove(), 300);
    }, duration);
  },

  /* ── Stat Card Builder ─────────────────────────────── */
  statCard({ label, value, trend, trendDir = 'up', icon, accent = '' }) {
    return `
      <div class="stat-card ${accent}">
        <div class="stat-icon">${this.icon(icon || 'activity', 20)}</div>
        <div class="stat-label">${label}</div>
        <div class="stat-value">${value}</div>
        ${trend ? `<div class="stat-trend ${trendDir}">
          ${this.icon(trendDir === 'up' ? 'arrowUp' : 'arrowDown', 12)} ${trend}
        </div>` : ''}
      </div>
    `;
  },

