  /* ── Gauge Ring (SVG) ──────────────────────────────── */
  gaugeRing(percent, size = 80, color = 'var(--accent-cyan)', label = '') {
    const r = (size - 12) / 2;
    const c = 2 * Math.PI * r;
    const offset = c - (percent / 100) * c;
    return `
      <div class="gauge-ring" style="width:${size}px;height:${size}px">
        <svg width="${size}" height="${size}">
          <circle class="gauge-bg" cx="${size/2}" cy="${size/2}" r="${r}" stroke-width="6"/>
          <circle class="gauge-fill" cx="${size/2}" cy="${size/2}" r="${r}"
            stroke="${color}" stroke-width="6"
            stroke-dasharray="${c}" stroke-dashoffset="${offset}"
            style="transition:stroke-dashoffset .8s cubic-bezier(0.16,1,0.3,1)"/>
        </svg>
        <div class="gauge-value">${Math.round(percent)}%</div>
      </div>
    `;
  },

  /* ── Badge ─────────────────────────────────────────── */
  badge(text, variant = 'cyan', dot = false) {
    return `<span class="badge badge-${variant} ${dot ? 'badge-dot' : ''}">${text}</span>`;
  },

  /* ── Section Header ────────────────────────────────── */
  sectionHeader(title, subtitle = '', actions = '') {
    return `
      <div class="section-header">
        <div>
          <h3>${title}</h3>
          ${subtitle ? `<p class="section-subtitle">${subtitle}</p>` : ''}
        </div>
        ${actions ? `<div class="flex items-center gap-3">${actions}</div>` : ''}
      </div>
    `;
  },

  /* ── Format Helpers ────────────────────────────────── */
  formatNumber(n) {
    if (n >= 1e7) return (n / 1e7).toFixed(1) + 'Cr';
    if (n >= 1e5) return (n / 1e5).toFixed(1) + 'L';
    if (n >= 1e3) return (n / 1e3).toFixed(1) + 'K';
    return n.toLocaleString();
  },

  formatTime(date) {
    return date.toLocaleTimeString('en-IN', { hour: '2-digit', minute: '2-digit', hour12: true });
  },

  formatDate(date) {
    return date.toLocaleDateString('en-IN', { day: 'numeric', month: 'short', year: 'numeric' });
  }
